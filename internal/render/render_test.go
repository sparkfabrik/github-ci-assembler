// ---------------------------------------------------------------------------
// TestRender_DisplayNameRendered
// ---------------------------------------------------------------------------

func TestRender_DisplayNameRendered(t *testing.T) {
	jobs := []*config.AssembledJob{
		{
			ID:          "build--drupal--docker-php",
			Stage:       "build",
			DisplayName: "Build PHP - drupal [build]",
			Properties:  map[string]any{"runs-on": "ubuntu-latest"},
		},
	}

	out := mustRender(t, minimalResult(jobs))

	if !strings.Contains(out, "name: Build PHP - drupal [build]") {
		t.Errorf("expected display name in output:\n%s", out)
	}

	// Verify name appears right after the job ID line.
	lines := strings.Split(out, "\n")
	for i, line := range lines {
		if strings.Contains(line, "build--drupal--docker-php:") {
			if i+1 >= len(lines) {
				t.Fatal("job ID is on the last line, no name follows")
			}
			nextLine := strings.TrimSpace(lines[i+1])
			if !strings.HasPrefix(nextLine, "name:") {
				t.Errorf("expected 'name:' immediately after job ID, got: %q", nextLine)
			}
			break
		}
	}
}
